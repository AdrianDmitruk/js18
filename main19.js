// Напишите код, который просит пользователя ввести число m от
// 1 до 10 и выводит в консоль первые m чисел Фибоначчи.

const m = +prompt("Введите число m (от 1 до 10):");

const fib = [0, 1];

for (let i = 2; i < m; i++) {
  fib[i] = fib[i - 1] + fib[i - 2];
}

console.log(fib.join(", "));

