//Задача 4

// Напишите код, который просит пользователя ввести количество строк -
// значение lines (от 1 до 9). Ему в консоль выводится пирамидка
// следующей структуры:

const lines = +prompt("Введите количество строк (от 1 до 9):");

// for

for (let i = 1; i <= lines; i++) {
  let pyramid = " ".repeat(lines - i) + "#".repeat(2 * i - 1);
  console.log(pyramid);
}

// while

let i = 1;
while (i <= lines) {
  let pyramid = " ".repeat(lines - i) + "#".repeat(2 * i - 1);
  console.log(pyramid);
  i++;
}

